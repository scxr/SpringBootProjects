package com.tph.demo.Pixel;

public class Pixel {
    private Integer xval;
    private Integer yval;
    private Integer rgb;
    public Integer getXval() {
        return xval;
    }

    public void setXval(Integer xval) {
        this.xval = xval;
    }

    public Integer getYval() {
        return yval;
    }

    public void setYval(Integer yval) {
        this.yval = yval;
    }

    public Integer getRgb() {
        return rgb;
    }

    public void setRgb(Integer rgb) {
        this.rgb = rgb;
    }
}
